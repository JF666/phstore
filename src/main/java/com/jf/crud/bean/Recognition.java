package com.jf.crud.bean;

public class Recognition {
    private Integer recid;

    private Integer facerec;

    private Integer scrfingerrec;

    private Integer rearfingerrec;

    private Integer sidefingerrec;

    private Integer frontfingerrec;

    public Integer getRecid() {
        return recid;
    }

    public void setRecid(Integer recid) {
        this.recid = recid;
    }

    public Integer getFacerec() {
        return facerec;
    }

    public void setFacerec(Integer facerec) {
        this.facerec = facerec;
    }

    public Integer getScrfingerrec() {
        return scrfingerrec;
    }

    public void setScrfingerrec(Integer scrfingerrec) {
        this.scrfingerrec = scrfingerrec;
    }

    public Integer getRearfingerrec() {
        return rearfingerrec;
    }

    public void setRearfingerrec(Integer rearfingerrec) {
        this.rearfingerrec = rearfingerrec;
    }

    public Integer getSidefingerrec() {
        return sidefingerrec;
    }

    public void setSidefingerrec(Integer sidefingerrec) {
        this.sidefingerrec = sidefingerrec;
    }

    public Integer getFrontfingerrec() {
        return frontfingerrec;
    }

    public void setFrontfingerrec(Integer frontfingerrec) {
        this.frontfingerrec = frontfingerrec;
    }
}